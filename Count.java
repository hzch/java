public class Count
{
	public static void main(String[] args)
	{
		int a = 21, b = 5;
		double c = 5.0, d = 5.2;
		System.out.println("a=21, b=5, c=5.0, d=5.2");
		System.out.println("a/b = " + a / b);
		System.out.println("a%b = " + a % b);
		System.out.println("a/c = " + a / c);
		System.out.println("a%d = " + a % d);
	}
}